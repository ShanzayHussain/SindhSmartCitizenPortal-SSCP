
CREATE TABLE USERS (
  USER_ID         NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  SUPABASE_UID    VARCHAR2(36),
  FULL_NAME       VARCHAR2(100)  NOT NULL,
  EMAIL           VARCHAR2(255)  NOT NULL,
  PASSWORD        VARCHAR2(255)  NOT NULL,
  CNIC            VARCHAR2(15),
  PHONE           VARCHAR2(20),
  ADDRESS         VARCHAR2(255),
  ROLE            VARCHAR2(30)   DEFAULT 'User' NOT NULL,
  CREATED_AT      TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
  USER_PROFILEPIC CLOB,
  FATHER_NAME     VARCHAR2(100),
  DISTRICT        VARCHAR2(100),
  DOB             DATE
);

CREATE UNIQUE INDEX USERS_EMAIL_UX ON USERS (EMAIL);
CREATE UNIQUE INDEX USERS_CNIC_UX  ON USERS (CNIC);
CREATE UNIQUE INDEX USERS_SUPABASE_UID_UX ON USERS (SUPABASE_UID);
COMMIT;

