-- ─── COMPLAINTS ──────────────────────────────────────────────────
CREATE TABLE COMPLAINTS (
  COMPLAINT_ID  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  USER_ID       NUMBER        NOT NULL,
  DEPT_ID       NUMBER        NOT NULL,
  OFFICER_ID    NUMBER,
  TITLE         VARCHAR2(200) NOT NULL,
  DESCRIPTION   CLOB,
  ADMIN_REMARKS CLOB,
  STATUS        VARCHAR2(20)  DEFAULT 'Pending'
                CHECK (STATUS IN ('Pending', 'In Progress', 'Resolved', 'Closed')),
  DATE_FILED    DATE          DEFAULT SYSDATE NOT NULL,
  DATE_RESOLVED DATE,
  CONSTRAINT FK_COMP_USER    FOREIGN KEY (USER_ID)    REFERENCES USERS(USER_ID),
  CONSTRAINT FK_COMP_DEPT    FOREIGN KEY (DEPT_ID)    REFERENCES DEPARTMENTS(DEPT_ID),
  CONSTRAINT FK_COMP_OFFICER FOREIGN KEY (OFFICER_ID) REFERENCES OFFICERS(OFFICER_ID)
);


-- ─── COMPLAINT DOCUMENTS (uploads) ───────────────────────────────
CREATE TABLE COMPLAINT_DOCUMENTS (
  DOC_ID       NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  COMPLAINT_ID NUMBER       NOT NULL,
  FILE_NAME    VARCHAR2(255),
  FILE_DATA    CLOB,              -- base64-encoded file content
  UPLOADED_AT  TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT FK_DOC_COMPLAINT FOREIGN KEY (COMPLAINT_ID) REFERENCES COMPLAINTS(COMPLAINT_ID)
);

COMMIT;



